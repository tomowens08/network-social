      &quot;Next.&quot; </font></li>
      <li><font size="2">Select the &quot;Contacts&quot; folder to export from and click 
      &quot;Next.&quot; </font></li>
      <li><font size="2">Specify a location where you would like to save the 
      file, and enter a name for the .CSV file. Then click &quot;Next.&quot; </font></li>
      <li><font size="2">Make sure to check the box next to &quot;Export Contacts 
      from the Contacts Folder,&quot; and then click &quot;Finish&quot; to export the data to 
      the .CSV file. </font></li>
    </ol>
    <font size="2">IMPORT TO Music.fm-</font><p><font size="2">Once you've
    exported your email address book into .CSV format login to Music.fm and go
