    Exporting from Outlook Express to </font></b></font><b>
    <font size="2" face="Arial">Music.fm Address Book</font></b></td>
  </tr>
  <tr>
    <td class="mbody" vAlign="top"><font size="2"><!-- answer -->To copy contact 
    information from Microsoft Outlook Express to <b>Music.fm Address Book</b>,
    please follow these steps: </font>
    <p><font size="2">EXPORT-</font></p>
    <p><font size="2">First, export information from Outlook Express: </font>
    </p>
    <ol>
      <li><font size="2">Open Outlook Express. </font></li>
      <li><font size="2">Select &quot;Export&quot; from the &quot;File&quot; menu. </font></li>
      <li><font size="2">From the &quot;Export&quot; menu, select &quot;Address Book.&quot; </font>
      </li>
      <li><font size="2">A dialog box opens and asks: &quot;Click the exported file 
      as:&quot; Enter a file name and then click browse to designate where the file 
      is saved.&nbsp; Enter a location where the file will be saved and a name 
      for the file <b>exportemail </b>and saving it on your desktop so you can 
      find it easy). </font></li>
      <li><font size="2">Click the &quot;Next&quot; button. </font></li>
      <li><font size="2">Check which fields you wish to export (<b>make sure you 
      include the email address field as one of the fields to export!</b> we 
      suggest exporting email address field, and first name, last name fields at 
      least so you have the first name, last name and email of your contacts).&nbsp; 
      Untick any fields you don't want to export.</font></li>
      <li><font size="2">Click &quot;Finish&quot; to export the data to the Comma 
      Separated Values (.CSV) file at the location you specified.&nbsp; </font>
      </li>
    </ol>
    <font size="2">IMPORT TO Music.fm-</font><p><font size="2">Once you've
    exported your email address book into .CSV format login to Music.fm and go
    to your <b>Music.fm Address Book</b>. </font></p>
    <p><font size="2">Next, import the .CSV file into your <b>Music.fm Address
    Book:</b> </font></p>
    <ol>
      <li><font size="2">Make sure that you are signed into your Music.fm
      account. Go to your <b>Music.fm Address Book</b>.</font></li>
      <li><font size="2">Click the &quot;Addresses Options&quot; link on the Address Book 
      page. </font></li>
      <li><font size="2">Click the &quot;Import&quot; link. </font></li>
      <li><font size="2">In the Import section, select &quot;Microsoft Outlook (.CSV 
      file).&quot; </font></li>
      <li><font size="2">Click the &quot;Browse&quot; button to find the file on your 
      system. If you saved it on your desktop it'll be easy to find.</font></li>
      <li><font size="2">Locate the file you saved and click the &quot;Import&quot; 
      button. It may take a few minutes to upload, especially if you use a 
      dialup internet connection.&nbsp; </font></li>
      <li><font size="2">Once uploaded you should be able to see your contacts 
      in your <b>Music.fm Address Book</b>.&nbsp; </font></li>
      <li><font size="2">Now you're ready to go to the <b>Invite Friends</b> 
      link to invite people to join your network, inserting their email 
      addresses from your <b>Music.fm Address Book</b>.</font></li>
    </ol>
    <p><b><font size="2">Please Note:</font></b><font size="2"> Any problems 
    exporting from Outlook Express please refer to the Outlook Express help menu 
    in the Outlook Express application.</font></p>
    <p>&nbsp;</td>
