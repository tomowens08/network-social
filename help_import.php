      account. Go to your <b>Music.fm Address Book</b>.</font></li>
      <li><font size="2">Click the &quot;Addresses Options&quot; link on the Address Book 
      page. </font></li>
      <li><font size="2">Click the &quot;Import&quot; link. </font></li>
      <li><font size="2">In the Import section, select &quot;Microsoft Outlook (.CSV 
      file).&quot; </font></li>
      <li><font size="2">Click the &quot;Browse&quot; button to find the file on your 
      system. If you saved it on your desktop it'll be easy to find.</font></li>
      <li><font size="2">Locate the file you saved and click the &quot;Import&quot; 
      button. It may take a few minutes to upload, especially if you use a 
      dialup internet connection.&nbsp; Once uploaded you should be able to see 
